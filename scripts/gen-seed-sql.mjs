// Generates seed.sql from the shared curated role data.
// Run with:  node scripts/gen-seed-sql.mjs   (writes ./seed.sql)
// Then paste seed.sql into the Supabase SQL editor and run it.

import { writeFileSync } from "node:fs";
import { ROLES } from "./roles-data.mjs";

const q = (v) => {
  if (v === null || v === undefined) return "null";
  if (typeof v === "boolean") return v ? "true" : "false";
  if (typeof v === "number") return String(v);
  return `'${String(v).replace(/'/g, "''")}'`; // escape single quotes
};

// Postgres text[] literal, e.g. ARRAY['a','b']::text[]
const arr = (a) =>
  !a || a.length === 0
    ? "ARRAY[]::text[]"
    : `ARRAY[${a.map((s) => `'${String(s).replace(/'/g, "''")}'`).join(", ")}]::text[]`;

const cols = [
  "company",
  "title",
  "archetype",
  "real_pm_score",
  "real_pm_signals",
  "is_live",
  "freshness_checked_at",
  "location",
  "jd_text",
  "crowd_response_days",
  "has_warm_path",
  "warm_path_note",
];

const values = ROLES.map((r) => {
  const row = [
    q(r.company),
    q(r.title),
    q(r.archetype),
    q(r.real_pm_score),
    arr(r.real_pm_signals),
    q(r.is_live),
    q(r.freshness_checked_at),
    q(r.location),
    q(r.jd_text),
    q(r.crowd_response_days),
    q(r.has_warm_path),
    q(r.warm_path_note),
  ];
  return `  (${row.join(", ")})`;
}).join(",\n");

const sql = `-- Product Compass — seed for the shared \`roles\` table (Stage 2).
-- Generated by scripts/gen-seed-sql.mjs. Run in the Supabase SQL editor
-- (runs as service-role, so it bypasses RLS).
-- Idempotent: clears existing roles first.

truncate table public.roles;

insert into public.roles
  (${cols.join(", ")})
values
${values};
`;

writeFileSync(new URL("../seed.sql", import.meta.url), sql, "utf8");
console.log(`Wrote seed.sql with ${ROLES.length} roles.`);
